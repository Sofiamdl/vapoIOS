import Foundation

class Card {
    let image: String
    let carStop: String
    let city: String
    let district: String
    let street: String
    let number: String
    let extra: String

    init(image: String, carStop: String, city: String, district: String, street: String,  number: String, extra: String){
        self.image = image
        self.carStop = carStop
        self.city = city
        self.district = district
        self.street = street
        self.number = number
        self.extra = extra
    }
}





